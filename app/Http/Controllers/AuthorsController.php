use App\Author;

use DB;

class AuthorsController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
    	$authors = Author::all();
        return view('authors/authors', ['authors' => $authors]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('authors/authors-create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
    	//TODO: validation should be done in the MODEL not in the controller :)
        $data = Input::all();
		$rules = ['name' => 'required'];
    	$validator = Validator::make($data, $rules);
		if ($validator->fails()) {
			return Redirect('/authors/create')->withErrors($validator)->withInput();
		}
		else {
			echo "data is ok";
			
			Author::saveAuthor($data);
			
			return Redirect('/authors');
		}
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return view('authors/author-edit', ['id' => $id]);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        //TODO: validation in the model
		$data = Input::all();
		
		Author::editAuthor($data, $id);
		
		return Redirect('/authors');
    }

    /**
